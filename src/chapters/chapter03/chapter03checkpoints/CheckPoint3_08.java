public class CheckPoint3_08 {
	public static void main(String[] args) {
		int x =3;
		int y =4;
		int z ;
		
		if (x > 2) {
			  if (y > 2) {
			    z = x + y;
			    System.out.println("z is " + z);
			  }
			}
			else
			  System.out.println("x is " + x);
		int a =2;
		int b =2;
		int c ;
		
		if (a > 2) {
			  if (b > 2) {
			    c = a + b;
			    System.out.println("c is " + c);
			  }
			}
			else
			  System.out.println("a is " + a);
	}

}
