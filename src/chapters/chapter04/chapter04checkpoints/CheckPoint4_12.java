package chapters.chapter04.chapter04checkpoints;

public class CheckPoint4_12 {
	public static void main(String[] args) {
		char c = 'A';
		int i = (int)c;
		System.out.println(i);
		
		float f = 1000.34f;
		int a = (int)f;
		System.out.println(a); 
		
		double d = 1000.34;
		int b = (int)d;
		System.out.println(b); 
		
		int k = 97;
		char l = (char)k;
		System.out.println(l);
		
	}

}
